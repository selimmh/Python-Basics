# sorting a list

months = ['january', 'february', 'march', 'april', 'may']

# this will sort list items in alphabetic order
months.sort()
print(months)

# this will sort list items reverse in alphabetic order
months.sort(reverse=True)
print(months)


# let's refresh that list and print reverse
months = ['january', 'february', 'march', 'april', 'may']
print(months)

# this will sort list reverse
months.reverse()
print(months)